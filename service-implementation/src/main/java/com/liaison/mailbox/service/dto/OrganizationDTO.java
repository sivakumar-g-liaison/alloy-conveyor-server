package com.liaison.mailbox.service.dto;

/**
 * Class that contains organization details.
 *
 * @author OFS
 */
public class OrganizationDTO {

    private String id;
    private String name;

    public OrganizationDTO(String id, String name) {
        super();
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
