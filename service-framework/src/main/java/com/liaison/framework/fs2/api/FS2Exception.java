package com.liaison.framework.fs2.api;

/**
 * @author robert.christian
 */
public class FS2Exception extends Exception {

  private static final long serialVersionUID = -271984410782388358L;

  public FS2Exception() {
    super();
  }

  public FS2Exception(String arg0) {
    super(arg0);
  }

  public FS2Exception(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

  public FS2Exception(Throwable arg0) {
    super(arg0);
  }

}
